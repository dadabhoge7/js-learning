var dayNum = 8;

switch (dayNum) {
  case 1:
    console.log(`Mon`);
    break;
  case 2:
    console.log(`Tue`);
    break;
  case 3:
    console.log(`Wen`);
    break;
  case 4:
    console.log(`Thu`);
    break;
  case 5:
    console.log(`Fri`);
    break;
  case 6:
    console.log(`Sat`);
    break;
  case 7:
    console.log(`Sun`);
    break;

  default:
    console.log(`invalid ${dayNum} `);
    break;
}
