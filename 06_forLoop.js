console.log(`------------Print number 0 to 10----------------`);
for (var i = 0; i <= 10; i++) {
    console.log(i);
}

console.log(`------------Print number 10 to 0----------------`);
for (var i = 10; i >= 10; i--) {
    console.log(i);
}

console.log(`------------Print Even number 0 to 10----------------`);
for (var i = 0 ; i <= 10; i=i+2) {
    console.log(i);
}

console.log(`------------Print Even number 50 to 40----------------`);
for (var i = 50 ; i >= 40; i=i-2) {
    console.log(i);
}

console.log(`------------Print  number 5, 10,15,.... 40----------------`);
for (var i = 5 ; i <= 50; i=i+5) {
    console.log(i);
}